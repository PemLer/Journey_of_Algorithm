class Solution:
    def longestPalindromeSubseq(self, s: str) -> int:
        """
        i == j
          dp[i][j] = dp[i+1][j-1] + 2
        otherwise
          dp[i][j] = max(dp[i+1][j], dp[i][j-1])
        """
        n = len(s)
        dp = [[0] * n for _ in range(n)]
        for i in range(n):
            dp[i][i] = 1

        for j in range(1, n):
            for i in range(j-1, -1, -1):
                if s[i] == s[j]:
                    dp[i][j] = dp[i+1][j-1] + 2
                else:
                    dp[i][j] = max(dp[i + 1][j], dp[i][j - 1])
        return dp[0][-1]